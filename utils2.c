#include "ft_printf.h"

int		ft_nbr_size_int(int nb)
{
	int i;

	i = 0;
	while (nb / 10 != 0)
	{
		nb /= 10;
		i++;
	}
	return (i + 1);
}

char	istype(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' || c == 'v'
		|| c == 'u' || c == 'x' || c == 'X' || c == '%')
		return (1);
	return (0);
}
